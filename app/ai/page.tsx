'use client';

import { useState } from 'react';
import Nav from '@/components/Nav';
import Card from '@/components/Card';
import { languages } from '@/lib/languages';

export default function AiInvoicePage() {
  const [text, setText] = useState('Create an invoice for Ahmed for garden cleaning, 3 hours, $180, due next Friday.');
  const [language, setLanguage] = useState('English');
  const [result, setResult] = useState<any>(null);
  const [loading, setLoading] = useState(false);

  async function generate() {
    setLoading(true);
    const res = await fetch('/api/ai-invoice', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ text, language, currency: 'AUD' }),
    });
    setResult(await res.json());
    setLoading(false);
  }

  const total = result?.items?.reduce(
    (sum: number, item: any) => sum + Number(item.quantity) * Number(item.unitPrice),
    0
  );

  return (
    <main className="min-h-screen bg-[#050713] text-white">
      <Nav />

      <section className="mx-auto max-w-7xl px-5 py-8">
        <div className="mb-8 rounded-[2rem] border border-violet-500/30 bg-gradient-to-br from-violet-700 via-blue-700 to-slate-950 p-8 shadow-2xl">
          <p className="mb-3 text-sm font-bold uppercase tracking-widest text-violet-200">
            Invoix AI Assistant
          </p>
          <h1 className="max-w-3xl text-4xl font-black leading-tight md:text-6xl">
            Create professional invoices in seconds.
          </h1>
          <p className="mt-4 max-w-2xl text-lg text-slate-200">
            Type naturally in your own words. AI turns messy job notes into a clean invoice.
          </p>
        </div>

        <div className="grid gap-6 lg:grid-cols-2">
          <Card>
            <h2 className="text-2xl font-black">Describe the job</h2>
            <p className="mt-2 text-slate-400">
              Example: “Invoice Mohamed for pressure washing driveway, 2 hours, $220, due Friday.”
            </p>

            <label className="mt-6 block text-sm font-bold text-slate-300">
              Invoice language
            </label>

            <select
              value={language}
              onChange={(e) => setLanguage(e.target.value)}
              className="mt-2 w-full cursor-pointer rounded-2xl border border-slate-700 bg-white p-4 text-black outline-none"
            >
              {languages.map((l) => (
                <option key={l.code} value={l.label}>
                  {l.flag} {l.label} — {l.nativeName}
                </option>
              ))}
            </select>

            <label className="mt-6 block text-sm font-bold text-slate-300">
              Job details
            </label>

            <textarea
              value={text}
              onChange={(e) => setText(e.target.value)}
              className="mt-2 min-h-[210px] w-full rounded-2xl border border-slate-700 bg-[#171A33] p-4 text-white outline-none"
            />

            <button
              onClick={generate}
              disabled={loading}
              className="mt-5 w-full rounded-2xl bg-gradient-to-r from-violet-600 to-blue-500 py-4 text-lg font-black disabled:opacity-60"
            >
              {loading ? 'Generating invoice...' : 'Generate Invoice'}
            </button>
          </Card>

          <Card>
            <h2 className="text-2xl font-black">Live invoice preview</h2>

            {!result ? (
              <div className="mt-6 rounded-3xl border border-dashed border-slate-700 p-10 text-center text-slate-400">
                Your invoice preview will appear here.
              </div>
            ) : (
              <div className="mt-6 rounded-3xl bg-white p-6 text-slate-900 shadow-2xl">
                <div className="flex justify-between gap-4">
                  <div>
                    <h3 className="text-3xl font-black">INVOICE</h3>
                    <p className="text-sm text-slate-500">Generated by Invoix AI</p>
                  </div>
                  <div className="text-right text-sm">
                    <p><b>Currency:</b> {result.currency}</p>
                    <p><b>Due:</b> {result.dueDateText}</p>
                  </div>
                </div>

                <div className="mt-8 rounded-2xl bg-slate-100 p-4">
                  <p className="text-sm font-bold text-slate-500">BILL TO</p>
                  <p className="mt-1 text-xl font-black">{result.customerName}</p>
                  <p>{result.customerPhone}</p>
                  <p>{result.customerEmail}</p>
                </div>

                <table className="mt-8 w-full border-collapse text-sm">
                  <thead>
                    <tr className="border-b bg-slate-900 text-white">
                      <th className="p-3 text-left">Description</th>
                      <th className="p-3 text-right">Qty</th>
                      <th className="p-3 text-right">Price</th>
                      <th className="p-3 text-right">Total</th>
                    </tr>
                  </thead>
                  <tbody>
                    {result.items?.map((item: any, i: number) => (
                      <tr key={i} className="border-b">
                        <td className="p-3">{item.description}</td>
                        <td className="p-3 text-right">{item.quantity}</td>
                        <td className="p-3 text-right">${item.unitPrice}</td>
                        <td className="p-3 text-right font-bold">
                          ${Number(item.quantity) * Number(item.unitPrice)}
                        </td>
                      </tr>
                    ))}
                  </tbody>
                </table>

                <div className="mt-8 flex justify-end">
                  <div className="rounded-2xl bg-emerald-600 px-6 py-4 text-right text-white">
                    <p className="text-sm">Total Due</p>
                    <p className="text-3xl font-black">${total}</p>
                  </div>
                </div>

                <p className="mt-6 text-slate-600">{result.notes}</p>
              </div>
            )}

            {result && (
              <button
                onClick={() => window.print()}
                className="mt-5 w-full rounded-2xl border border-slate-700 py-4 font-black"
              >
                Print / Save PDF
              </button>
            )}
          </Card>
        </div>
      </section>
    </main>
  );
      }
